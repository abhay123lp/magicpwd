package com.magicpwd._comn;

/**
 * @author Amon
 */
public class C1C1
{

    private char k;
    private char v;

    public C1C1(char k, char v)
    {
        this.k = k;
        this.v = v;
    }

    /**
     * @return the k
     */
    public char getK()
    {
        return k;
    }

    /**
     * @param k
     *            the k to set
     */
    public void setK(char k)
    {
        this.k = k;
    }

    /**
     * @return the v
     */
    public char getV()
    {
        return v;
    }

    /**
     * @param v
     *            the v to set
     */
    public void setV(char v)
    {
        this.v = v;
    }
}
